# integers can be of any length, a floating point number is accurate only up
# to 15 decimal places (the 16th place is inaccurate)

# # Number systems  - binary(base 2), Octal(8), decimal(10), and Hexadecimal(16)
# Binary	    '0b' or '0B'
# Octal	        '0o' or '0O'
# Hexadecimal	'0x' or '0X'

# Output: 107
print(0b1101011)

# Output: 253 (251 + 2)
print(0xFB + 0b10)

# Output: 13
print(0o15)
